//! Colcon post-install hook generation.
//!
//! After cmake install (or pip install) completes, this module generates
//! the colcon-layer files that cmake/ament_cmake does NOT generate:
//!
//! - `hook/cmake_prefix_path.{dsv,sh}` — CMAKE_PREFIX_PATH
//! - `hook/pkg_config_path.{dsv,sh}` — PKG_CONFIG_PATH
//! - `hook/pkg_config_path_multiarch.{dsv,sh}` — multiarch variant
//! - `hook/ros_package_path.{dsv,sh}` — ROS_PACKAGE_PATH
//! - `share/colcon-core/packages/<name>` — runtime dependency list
//! - `.catkin` — legacy marker
//!
//! The ament layer (ament_index, environment/, local_setup, package.xml)
//! is generated by ament_cmake during `cmake --install`. We do NOT
//! duplicate or overwrite those files.

use std::path::Path;

use thiserror::Error;

#[derive(Debug, Error)]
pub enum PostInstallError {
    #[error("failed to write {path}: {source}")]
    Io {
        path: String,
        #[source]
        source: std::io::Error,
    },
}

fn write_file(path: &Path, content: &str) -> Result<(), PostInstallError> {
    if let Some(parent) = path.parent() {
        std::fs::create_dir_all(parent).map_err(|e| PostInstallError::Io {
            path: parent.display().to_string(),
            source: e,
        })?;
    }
    std::fs::write(path, content).map_err(|e| PostInstallError::Io {
        path: path.display().to_string(),
        source: e,
    })
}

/// Run colcon post-install for a single package.
///
/// Call this AFTER cmake install (or pip install) completes. It generates
/// only the colcon-layer files — the ament layer is left to cmake.
pub fn post_install(
    install_base: &Path,
    pkg_name: &str,
    runtime_deps: &[String],
) -> Result<(), PostInstallError> {
    generate_colcon_hooks(install_base, pkg_name)?;
    write_runtime_deps(install_base, pkg_name, runtime_deps)?;
    write_catkin_marker(install_base)?;
    Ok(())
}

// ── colcon-core/packages ─────────────────────────────────────────────────────

/// Write the colcon runtime dependencies file.
///
/// `<install>/share/colcon-core/packages/<name>` containing colon-separated
/// runtime dependency names. Used by `_local_setup_util_sh.py` for
/// topological order sourcing.
fn write_runtime_deps(
    install_base: &Path,
    pkg_name: &str,
    runtime_deps: &[String],
) -> Result<(), PostInstallError> {
    let path = install_base
        .join("share/colcon-core/packages")
        .join(pkg_name);
    write_file(&path, &runtime_deps.join(":"))
}

// ── hook/ directory ──────────────────────────────────────────────────────────

/// Generate colcon hook scripts in `share/<name>/hook/`.
///
/// These set `CMAKE_PREFIX_PATH`, `PKG_CONFIG_PATH`, and
/// `ROS_PACKAGE_PATH` — variables that ament_cmake does NOT set.
fn generate_colcon_hooks(install_base: &Path, pkg_name: &str) -> Result<(), PostInstallError> {
    let hook_dir = install_base.join("share").join(pkg_name).join("hook");

    // cmake_prefix_path — only if install contains Config.cmake files
    if has_cmake_config(install_base) {
        write_hook_dsv_sh(
            &hook_dir,
            "cmake_prefix_path",
            "prepend-non-duplicate",
            "CMAKE_PREFIX_PATH",
            "",
        )?;
    }

    // pkg_config_path
    write_hook_dsv_sh(
        &hook_dir,
        "pkg_config_path",
        "prepend-non-duplicate",
        "PKG_CONFIG_PATH",
        "lib/pkgconfig",
    )?;

    // pkg_config_path_multiarch
    if let Some(multiarch) = get_multiarch() {
        write_hook_dsv_sh(
            &hook_dir,
            "pkg_config_path_multiarch",
            "prepend-non-duplicate",
            "PKG_CONFIG_PATH",
            &format!("lib/{multiarch}/pkgconfig"),
        )?;
    }

    // ros_package_path
    write_hook_dsv_sh(
        &hook_dir,
        "ros_package_path",
        "prepend-non-duplicate",
        "ROS_PACKAGE_PATH",
        "share",
    )?;

    Ok(())
}

/// Write a DSV + sh hook file pair.
fn write_hook_dsv_sh(
    hook_dir: &Path,
    name: &str,
    action: &str,
    variable: &str,
    suffix: &str,
) -> Result<(), PostInstallError> {
    // DSV file
    let dsv = format!("{action};{variable};{suffix}");
    write_file(&hook_dir.join(format!("{name}.dsv")), &dsv)?;

    // Shell file
    let sh = if suffix.is_empty() {
        format!("_colcon_prepend_unique_value {variable} \"$COLCON_CURRENT_PREFIX\"\n")
    } else {
        format!("_colcon_prepend_unique_value {variable} \"$COLCON_CURRENT_PREFIX/{suffix}\"\n")
    };
    write_file(&hook_dir.join(format!("{name}.sh")), &sh)?;

    Ok(())
}

// ── .catkin marker ───────────────────────────────────────────────────────────

/// Write the `.catkin` legacy marker (empty file at install root).
fn write_catkin_marker(install_base: &Path) -> Result<(), PostInstallError> {
    let marker = install_base.join(".catkin");
    if !marker.exists() {
        write_file(&marker, "")?;
    }
    Ok(())
}

// ── helpers ──────────────────────────────────────────────────────────────────

/// Check if the install prefix contains any CMake config files.
fn has_cmake_config(install_base: &Path) -> bool {
    let walk = |dir: &Path| -> bool {
        if !dir.exists() {
            return false;
        }
        if let Ok(entries) = std::fs::read_dir(dir) {
            for entry in entries.flatten() {
                let name = entry.file_name();
                let name = name.to_string_lossy();
                if name.ends_with("Config.cmake") || name.ends_with("-config.cmake") {
                    return true;
                }
            }
        }
        false
    };
    // Check share/<pkg>/cmake/ and lib/cmake/
    for subdir in ["share", "lib"] {
        let cmake_dir = install_base.join(subdir);
        if cmake_dir.exists() {
            for entry in std::fs::read_dir(&cmake_dir)
                .into_iter()
                .flatten()
                .flatten()
            {
                if entry.path().is_dir()
                    && (walk(&entry.path()) || walk(&entry.path().join("cmake")))
                {
                    return true;
                }
            }
        }
    }
    false
}

/// Get the multiarch triplet (e.g. "x86_64-linux-gnu") for PKG_CONFIG_PATH.
fn get_multiarch() -> Option<String> {
    std::process::Command::new("gcc")
        .arg("-print-multiarch")
        .output()
        .ok()
        .filter(|o| o.status.success())
        .map(|o| String::from_utf8_lossy(&o.stdout).trim().to_owned())
        .filter(|s| !s.is_empty())
        .or_else(|| {
            std::process::Command::new("dpkg-architecture")
                .arg("-qDEB_HOST_MULTIARCH")
                .output()
                .ok()
                .filter(|o| o.status.success())
                .map(|o| String::from_utf8_lossy(&o.stdout).trim().to_owned())
                .filter(|s| !s.is_empty())
        })
}

#[cfg(test)]
mod tests {
    use super::*;
    use tempfile::TempDir;

    #[test]
    fn post_install_creates_colcon_markers() {
        let tmp = TempDir::new().unwrap();
        post_install(tmp.path(), "my_pkg", &["dep_a".into(), "dep_b".into()]).unwrap();

        // colcon-core/packages
        let deps_file = tmp.path().join("share/colcon-core/packages/my_pkg");
        assert!(deps_file.exists());
        assert_eq!(std::fs::read_to_string(&deps_file).unwrap(), "dep_a:dep_b");

        // .catkin
        assert!(tmp.path().join(".catkin").exists());
    }

    #[test]
    fn post_install_generates_hook_files() {
        let tmp = TempDir::new().unwrap();
        // Create a fake cmake config so cmake_prefix_path hook is generated
        let cmake_dir = tmp.path().join("share/my_pkg/cmake");
        std::fs::create_dir_all(&cmake_dir).unwrap();
        std::fs::write(cmake_dir.join("my_pkgConfig.cmake"), "").unwrap();

        post_install(tmp.path(), "my_pkg", &[]).unwrap();

        let hook = tmp.path().join("share/my_pkg/hook");
        assert!(hook.join("cmake_prefix_path.dsv").exists());
        assert!(hook.join("cmake_prefix_path.sh").exists());
        assert!(hook.join("pkg_config_path.dsv").exists());
        assert!(hook.join("pkg_config_path.sh").exists());
        assert!(hook.join("ros_package_path.dsv").exists());
        assert!(hook.join("ros_package_path.sh").exists());
    }

    #[test]
    fn cmake_prefix_path_hook_skipped_without_config() {
        let tmp = TempDir::new().unwrap();
        post_install(tmp.path(), "my_pkg", &[]).unwrap();

        let hook = tmp.path().join("share/my_pkg/hook");
        // No Config.cmake → no cmake_prefix_path hook
        assert!(!hook.join("cmake_prefix_path.dsv").exists());
        // Other hooks still present
        assert!(hook.join("pkg_config_path.dsv").exists());
    }

    #[test]
    fn dsv_format_matches_colcon() {
        let tmp = TempDir::new().unwrap();
        post_install(tmp.path(), "my_pkg", &[]).unwrap();

        let dsv = std::fs::read_to_string(tmp.path().join("share/my_pkg/hook/pkg_config_path.dsv"))
            .unwrap();
        assert_eq!(dsv, "prepend-non-duplicate;PKG_CONFIG_PATH;lib/pkgconfig");

        let dsv =
            std::fs::read_to_string(tmp.path().join("share/my_pkg/hook/ros_package_path.dsv"))
                .unwrap();
        assert_eq!(dsv, "prepend-non-duplicate;ROS_PACKAGE_PATH;share");
    }

    #[test]
    fn multiarch_hook_generated_on_linux() {
        let tmp = TempDir::new().unwrap();
        post_install(tmp.path(), "my_pkg", &[]).unwrap();

        // On Linux with gcc, multiarch hook should exist
        if get_multiarch().is_some() {
            let dsv = std::fs::read_to_string(
                tmp.path()
                    .join("share/my_pkg/hook/pkg_config_path_multiarch.dsv"),
            )
            .unwrap();
            assert!(dsv.contains("PKG_CONFIG_PATH"));
            assert!(dsv.contains("x86_64-linux-gnu") || dsv.contains("aarch64-linux-gnu"));
        }
    }
}
